public class Double1 
{
	public static void main(String[] args) 
	{
		long d = 255; //creating double type variable but assigning int type value
		long d1 = 4.5; //creating double type variable but assigning double type value
		long d2 = 4.5f; //creating double type variable but assigning float type value
		System.out.println(d);
		System.out.println(d1);
		System.out.println(d2);
	}
}
