public class Float1 
{
	public static void main(String[] args) 
	{
		long f = 255; //creating float type variable but assigning int type value
		long f1 = 4.5; //creating float type variable but assigning double type value
		long f2 = 4.5f; //creating float type variable but assigning float type value
		System.out.println(f);
		System.out.println(f1);
		System.out.println(f2);
	}
}
