public class Long1 
{
	public static void main(String[] args) 
	{
		long l = 255; //creating long type variable but assigning int type value
		long l1 = 4.5; //creating long type variable but assigning double type value
		long l2 = 4.5f; //creating long type variable but assigning float type value
		System.out.println(l);
		System.out.println(l1);
		System.out.println(l2);
	}
}
