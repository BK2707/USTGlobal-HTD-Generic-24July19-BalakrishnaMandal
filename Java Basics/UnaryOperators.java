public class UnaryOperators  
{
	public static void main(String[] args) 
	{
		int i = 10;
		int j = 10;
		System.out.println("INCREMENT");
		System.out.println(i++);
		System.out.println(++i);
		i++;
		System.out.println(i);
		++i;
		System.out.println(i);

		System.out.println("DECREMENT");
		System.out.println(j--);
		System.out.println(--j);
		j++;
		System.out.println(j);
		++j;
		System.out.println(j);

	}
}
