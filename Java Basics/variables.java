public class Variables 
{
	public static void main(String[] args) 
	{
		int i;					// deceleration
		i = 10;				   //initialization
		System.out.println("Integer: "+i); //utilization


		byte b;					//deceleration
		b = 127;				//initialization
		System.out.println("byte: "+b); //utilization

		short s;				//deceleration
		s = 125;				//initialization
		System.out.println("short: "+s); //utilization

		long l;					//deceleration
		l=668079090;			//initialization
		System.out.println("long: "+l); //utilization

		double d;				//deceleration
		d= 1425.25;				//initialization
		System.out.println("double: "+d); //utilization

		float f;				//deceleration
		f = 45025f;				//initialization
		System.out.println("float: "+f); //utilization

		char c;					//deceleration
		c = 'B';				//initialization
		System.out.println("char: "+c); //utilization

		boolean bo;				//deceleration
		bo = true;				//initialization
		System.out.println("boolean: "+bo); //utilization

	}
}
